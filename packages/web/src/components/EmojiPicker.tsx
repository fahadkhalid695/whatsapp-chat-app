const EmojiPicker: React.FC<EmojiPickerProps> = ({
  anchorEl,
  open,
  onClose,
  onEmojiSelect,
}) => {
  const [selectedCategory, setSelectedCategory] = useState('recent');
  const [searchQuery, setSearchQuery] = useState('');

  const handleEmojiClick = (emoji: string) => {
    onEmojiSelect(emoji);
    onClose();
  };

  const filteredEmojis = searchQuery
    ? Object.values(emojiCategories)
        .flatMap(category => category.emojis)
        .filter(emoji => 
          // Simple search - in a real app, you'd have emoji names/keywords
          emoji.includes(searchQuery)
        )
    : emojiCategories[selectedCategory as keyof typeof emojiCategories]?.emojis || [];

  return (
    <Popover
      open={open}
      anchorEl={anchorEl}
      onClose={onClose}
      anchorOrigin={{
        vertical: 'top',
        horizontal: 'left',
      }}
      transformOrigin={{
        vertical: 'bottom',
        horizontal: 'left',
      }}
      PaperProps={{
        sx: {
          width: 350,
          height: 400,
          borderRadius: 2,
        },
      }}
    >
      <Box sx={{ p: 2 }}>
        {/* Search */}
        <TextField
          fullWidth
          size="small"
          placeholder="Search emojis..."
          value={searchQuery}
          onChange={(e) => setSearchQuery(e.target.value)}
          InputProps={{
            startAdornment: (
              <InputAdornment position="start">
                <Search />
              </InputAdornment>
            ),
          }}
          sx={{ mb: 2 }}
        />

        {/* Category Tabs */}
        {!searchQuery && (
          <Tabs
            value={selectedCategory}
            onChange={(_, newValue) => setSelectedCategory(newValue)}
            variant="scrollable"
            scrollButtons="auto"
            sx={{ mb: 2, minHeight: 'auto' }}
          >
            {Object.entries(emojiCategories).map(([key, category]) => (
              <Tab
                key={key}
                value={key}
                label={category.emojis[0]}
                sx={{ minWidth: 'auto', minHeight: 'auto', p: 1 }}
              />
            ))}
          </Tabs>
        )}

        {/* Emoji Grid */}
        <Box
          sx={{
            height: searchQuery ? 280 : 240,
            overflow: 'auto',
          }}
        >
          <Grid container spacing={0.5}>
            {filteredEmojis.map((emoji, index) => (
              <Grid item key={index}>
                <IconButton
                  onClick={() => handleEmojiClick(emoji)}
                  sx={{
                    width: 40,
                    height: 40,
                    fontSize: '1.2rem',
                    '&:hover': {
                      bgcolor: '#f5f5f5',
                    },
                  }}
                >
                  {emoji}
                </IconButton>
              </Grid>
            ))}
          </Grid>
        </Box>
      </Box>
    </Popover>
  );
};

export default EmojiPicker;